<h4>O tvrtki</h4>
<p>
	Tvrtka ChangeIT j.d.o.o. je osnovana 2016. godine s namjerom pružanja kvalitetne i pravovremene usluge svim njenim korisnicima. Usluge koje pruža tvrtka su:
</p>
<ul>
	<li>
		Održavanje računala i računalnih mreža
	</li>
	<li>
		Servisiranje računala, računalnih komponenti i perifernih jedinica
	</li>
	<li>
		Sklapanje računala, servera te postavljanje serverske infrastrukture, lokalnih i wireless mreža
	</li>
	<li>
		Administriranje sustava i servera, te podešavanje mrežne infrastrukture
	</li>
	<li>
		Instaliranje operacijskih sustava, upravljačkih programa, Office paketa, posebnih komercijalnih programa
	</li>
	<li>
		Backupiranje, postavljanje antivirusne zaštite i firewalla (vatrozida)
	</li>
	<li>
		Prodaja računalnih komponenti i perifernih jedinica
	</li>
	<li>
		Prodaja tinti i tonera
	</li>
	<li>
		Savjetovanje vezano za nabavku i postavljanje računalne i mrežne infrastrukture
	</li>
</ul>
<p>
	Svaki klijent za nas je zasebna priča, stoga se svakom klijentu posebno posvećujemo kako bi udovoljili svim postavljenim zahtjevima. Uz našu podršku smatramo da se mogu izbjeći nepotrebni troškovi i ostvariti višestruke koristi. Svoje obaveze obavljamo u garantiranim rokovima i uvijek smo spremni dati dodatni ustupak ako smatramo da će time Vaša organizacija biti zadovoljna.
</p>